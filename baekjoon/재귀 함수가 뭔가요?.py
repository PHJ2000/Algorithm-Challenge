"""
사이트: 백준  
문제 이름: [재귀함수가 뭔가요?](https://www.acmicpc.net/problem/17478)

풀이 접근 방법:
- 재귀 함수를 사용해 출력 형식 그대로 구현
- 깊이(`depth`)에 따라 들여쓰기를 `____` 단위로 추가
- 종료 조건에서는 정해진 문장을 출력하고,
  이후 재귀가 종료되면서 `"라고 답변하였지."` 문장을 출력

느낀점:
- 단순 구현 문제처럼 보이지만, 출력 형식을 정확히 맞추는 연습에 유익했음
- 재귀 호출과 반환 순서를 다시금 떠올려볼 수 있어서 좋았음
"""
def recursive(n, depth):
    indent = "____" * depth
    print(f'{indent}"재귀함수가 뭔가요?"')

    if depth == n:
        print(f'{indent}"재귀함수는 자기 자신을 호출하는 함수라네"')
    else:
        print(f'{indent}"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.')
        print(f'{indent}마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.')
        print(f'{indent}그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어."')
        recursive(n, depth + 1)

    print(f'{indent}라고 답변하였지.')

# 입력 받기
n = int(input())
print("어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.")
recursive(n, 0)
